// Localized display for a stored enum value. Fail-open: an unmapped value (or a
// non-he language) returns the ORIGINAL value unchanged — never an empty string.
export function labelFor(category, value, lang = "en") {
  if (value == null || lang === "en") return value;
  const entry = DISPLAY_LABELS[category]?.[value];
  return (entry && entry[lang]) || value;
}

